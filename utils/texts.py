"""Единые текстовые шаблоны и хелперы для тёплого тона."""

def ack_done() -> str:
    return "Отметила, спасибо!"


def ack_error() -> str:
    return "Не поняла, попробуй ещё раз."


GENTLE_ON = (
    "Включила щадящий режим на сегодня: минимум задач, без напоминаний. "
    "Завтра само выключится. Вернуть сразу — кнопка «Щадящий режим» или команда /resume."
)

GENTLE_OFF = "Щадящий режим выключен, напоминания вернулись. Если захочешь снова — кнопка «Щадящий режим»."


def care_prompt(title: str) -> str:
    return f"{title}\nОтметь, когда сделаешь."
