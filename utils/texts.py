def ack(text: str) -> str:
    """Мягкое подтверждение действия."""
    return f"Готово: {text}"


def error(text: str) -> str:
    """Мягкая ошибка без шейминга."""
    return f"Не вышло: {text}. Попробуем ещё раз?"


def register_text() -> str:
    """Подсказка, что нужно пройти первичную настройку."""
    return (
        "Давай сначала познакомимся и настроим бота под тебя. "
        "Набери /start — я спрошу имя и часовой пояс один раз, дальше кнопок будет достаточно."
    )


# Щадящий режим / пауза
GENTLE_ON = (
    "Включила щадящий режим: сегодня напоминаний будет меньше, только самое базовое. "
    "Можно перевести дух, ничего страшного не случится."
)

GENTLE_OFF = (
    "Щадящий режим выключен, вернула обычные напоминания. "
    "Если снова станет тяжело — можно в любой момент включить его ещё раз."
)


def gentle_pause() -> str:
    return "Сейчас включён щадящий режим: я буду писать меньше. Вернуть — командой /resume или в Настройках."


def gentle_error_prefix() -> str:
    return "Я поправлюсь без ругани: "


def gentle_streak(streak: int) -> str:
    if streak <= 0:
        return "Сегодня новый старт, без стриков. Давай начнём с одного шага."
    if streak == 1:
        return "Стрик 1 день — это уже начало. Поддержу тебя."
    if streak < 7:
        return f"Стрик {streak} дней — держишь темп, уже заметно!"
    return f"Стрик {streak} дней — круто! Главное — без перегорания."


def gentle_skip() -> str:
    return "Отметила как пропуск. Если захочешь вернуться — просто выбери задачу снова."
